    //      LOAD ADMINS
    private void loadAdmins() {
        try {
            LinkedList<Utilisateur> list = utilisateurDAO.getAllAssistants(); 
            // ❗ Ton DAO n’a pas getAllAdmins → on va filtrer MANUELLEMENT

            DefaultTableModel model = (DefaultTableModel) tableAdmins.getModel();
            model.setRowCount(0);

            // ⚠️ Récupère TOUS les utilisateurs ROLE = ADMIN
            java.sql.Connection conn = db.Connection.connect();
            java.sql.Statement st = conn.createStatement();
            java.sql.ResultSet rs = st.executeQuery("SELECT * FROM Utilisateur WHERE role = 'ADMIN'");

            while (rs.next()) {
                model.addRow(new Object[]{
                        rs.getInt("id"),
                        rs.getString("nom"),
                        rs.getString("prenom"),
                        rs.getString("login")
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur chargement admins");
            e.printStackTrace();
        }
    }

    //      SEARCH ADMINS
    private void searchAdmins() {
        String keyword = txtSearch.getText().trim().toLowerCase();

        try {
            DefaultTableModel model = (DefaultTableModel) tableAdmins.getModel();
            model.setRowCount(0);

            java.sql.Connection conn = db.Connection.connect();
            java.sql.Statement st = conn.createStatement();
            java.sql.ResultSet rs = st.executeQuery("SELECT * FROM Utilisateur WHERE role = 'ADMIN'");

            while (rs.next()) {
                String fullName = (rs.getString("nom") + " " + rs.getString("prenom")).toLowerCase();

                if (fullName.contains(keyword)) {
                    model.addRow(new Object[]{
                            rs.getInt("id"),
                            rs.getString("nom"),
                            rs.getString("prenom"),
                            rs.getString("login")
                    });
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur recherche !");
        }
    }
