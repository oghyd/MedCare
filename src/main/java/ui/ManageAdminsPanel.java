    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String nom = JOptionPane.showInputDialog(this, "Nom :");
        if (nom == null) return;

        String prenom = JOptionPane.showInputDialog(this, "Prénom :");
        if (prenom == null) return;

        String login = JOptionPane.showInputDialog(this, "Login :");
        String pass = JOptionPane.showInputDialog(this, "Mot de passe :");

        try {
            Utilisateur admin = new Utilisateur();
            admin.setNom(nom);
            admin.setPrenom(prenom);
            admin.setLogin(login);
            admin.setPassword(pass);
            admin.setRole(Role.ADMIN);
            admin.setSpecialite(null);
            admin.setDisponible(true);

            if (utilisateurDAO.createUtilisateur(admin)) {
                JOptionPane.showMessageDialog(this, "Admin ajouté !");
                loadAdmins();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur ajout !");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int row = tableAdmins.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Sélectionnez un admin.");
            return;
        }

        int id = (int) tableAdmins.getValueAt(row, 0);

        try {
            Utilisateur admin = utilisateurDAO.findAssistantById(id); 
            // findAssistantById() retourne nom/prenom/login même si ROLE=ADMIN

            String nom = JOptionPane.showInputDialog(this, "Nom :", admin.getNom());
            if (nom == null) return;

            String prenom = JOptionPane.showInputDialog(this, "Prénom :", admin.getPrenom());
            String login = JOptionPane.showInputDialog(this, "Login :", admin.getLogin());
            String pass = JOptionPane.showInputDialog(this, "Mot de passe :", admin.getPassword());

            admin.setNom(nom);
            admin.setPrenom(prenom);
            admin.setLogin(login);
            admin.setPassword(pass);
            admin.setRole(Role.ADMIN);

            if (utilisateurDAO.updateUtilisateur(admin)) {
                JOptionPane.showMessageDialog(this, "Admin modifié !");
                loadAdmins();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur modification !");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnEditActionPerformed

