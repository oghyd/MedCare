    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
          try {
        utilisateur.setLogin(txtLogin.getText().trim());

        String pwd = new String(txtPassword.getPassword());
        if (!pwd.isEmpty()) {
            utilisateur.setPassword(pwd);
        }

        if (utilisateur.getRole() == Role.MEDECIN) {
            utilisateur.setSpecialite(txtSpecialite.getText().trim());
            utilisateur.setDisponible(chkDisponible.isSelected());
        }

        if (utilisateurDAO.updateUtilisateur(utilisateur)) {
            JOptionPane.showMessageDialog(this, "Profil mis à jour !");
        } else {
            JOptionPane.showMessageDialog(this, "Aucune modification effectuée.");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erreur de mise à jour !");
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        switch (utilisateur.getRole()) {
            case ADMIN -> new AdminDashboard(utilisateur).setVisible(true);
            case ASSISTANT -> new AssistantDashboard(utilisateur).setVisible(true);
            case MEDECIN -> new DoctorDashboard(utilisateur).setVisible(true);
        }
        this.dispose();
    }//GEN-LAST:event_btnBackActionPerformed

