    // Charger les infos selon le modèle réel
    private void loadUserInfo() {
        lblUserName.setText("Connecté : " + utilisateur.getNom() + " " + utilisateur.getPrenom());

        txtLogin.setText(utilisateur.getLogin());
        txtPassword.setText(""); // tu ne dois jamais afficher le vrai mdp

        // Si médecin → afficher champs supplémentaires
        if (utilisateur.getRole() == Role.MEDECIN) {
            txtSpecialite.setText(utilisateur.getSpecialite());
            chkDisponible.setSelected(utilisateur.isDisponible());
        } else {
            txtSpecialite.setEnabled(false);
            chkDisponible.setEnabled(false);
        }
    }


