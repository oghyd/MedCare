   private void loadDoctors() {
        try {
            LinkedList<Utilisateur> list = utilisateurDAO.getAllMedecins();

            DefaultTableModel model = (DefaultTableModel) tableDoctors.getModel();
            model.setRowCount(0);

            for (Utilisateur u : list) {
                model.addRow(new Object[]{
                        u.getId(),
                        u.getNom(),
                        u.getPrenom(),
                        u.getSpecialite(),
                        u.isDisponible() ? "Oui" : "Non"
                });
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erreur chargement médecins");
            e.printStackTrace();
        }
    }
 private void searchDoctors() {
        String keyword = txtSearch.getText().trim().toLowerCase();

        try {
            LinkedList<Utilisateur> list = utilisateurDAO.getAllMedecins();

            DefaultTableModel model = (DefaultTableModel) tableDoctors.getModel();
            model.setRowCount(0);

            for (Utilisateur u : list) {
                String fullName = (u.getNom() + " " + u.getPrenom()).toLowerCase();

                if (fullName.contains(keyword) ||
                        u.getNom().toLowerCase().contains(keyword) ||
                        u.getPrenom().toLowerCase().contains(keyword)) {

                    model.addRow(new Object[]{
                            u.getId(),
                            u.getNom(),
                            u.getPrenom(),
                            u.getSpecialite(),
                            u.isDisponible() ? "Oui" : "Non"
                    });

                }
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erreur recherche !");
        } }
