    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String nom = JOptionPane.showInputDialog(this, "Nom :");
    if (nom == null) return;

    String prenom = JOptionPane.showInputDialog(this, "Prénom :");
    if (prenom == null) return;

    String login = JOptionPane.showInputDialog(this, "Login :");
    if (login == null) return;

    String password = JOptionPane.showInputDialog(this, "Mot de passe :");
    if (password == null) return;

    String specialite = JOptionPane.showInputDialog(this, "Spécialité :");
    if (specialite == null) return;

    try {
        Utilisateur med = new Utilisateur();
        med.setNom(nom);
        med.setPrenom(prenom);
        med.setLogin(login);
        med.setPassword(password);
        med.setRole(Role.MEDECIN);
        med.setSpecialite(specialite);
        med.setDisponible(true);

        if (utilisateurDAO.createUtilisateur(med)) {
            JOptionPane.showMessageDialog(this, "Médecin ajouté !");
            loadDoctors();
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erreur ajout médecin !");
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int row = tableDoctors.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Sélectionnez un médecin.");
        return;
    }

    int id = (int) tableDoctors.getValueAt(row, 0);

    try {
        Utilisateur med = utilisateurDAO.findMedecinById(id);

        String nom = JOptionPane.showInputDialog(this, "Nom :", med.getNom());
        if (nom == null) return;

        String prenom = JOptionPane.showInputDialog(this, "Prénom :", med.getPrenom());
        if (prenom == null) return;

        String login = JOptionPane.showInputDialog(this, "Login :", med.getLogin());
        if (login == null) return;

        String pass = JOptionPane.showInputDialog(this, "Mot de passe :", med.getPassword());
        if (pass == null) return;

        String specialite = JOptionPane.showInputDialog(this, "Spécialité :", med.getSpecialite());
        if (specialite == null) return;

        String dispoStr = JOptionPane.showInputDialog(this, "Disponible (oui/non) :", med.isDisponible() ? "oui" : "non");
        if (dispoStr == null) return;

        med.setNom(nom);
        med.setPrenom(prenom);
        med.setLogin(login);
        med.setPassword(pass);
        med.setSpecialite(specialite);
        med.setDisponible(dispoStr.equalsIgnoreCase("oui"));

        if (utilisateurDAO.updateUtilisateur(med)) {
            JOptionPane.showMessageDialog(this, "Médecin modifié !");
            loadDoctors();
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erreur modification médecin !");
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tableDoctors.getSelectedRow();
        if (row == -1) {
        JOptionPane.showMessageDialog(this, "Sélectionnez un médecin.");
        return;
    }

    int id = (int) tableDoctors.getValueAt(row, 0);

    int confirm = JOptionPane.showConfirmDialog(this,
            "Supprimer ce médecin ?", "Confirmation",
            JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        try {
            if (utilisateurDAO.deleteUtilisateur(id)) {
                JOptionPane.showMessageDialog(this, "Médecin supprimé !");
                loadDoctors();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur suppression médecin !");
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

